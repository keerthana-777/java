
public class demoswitchcase {
	public static void main(String args[]) {
		int s=5;
		
		switch(s) {
		case 1: 
			System.out.println("value 1");
			break;
		case 2: 
			System.out.println("value 2");
			break;
		case 3: 
			System.out.println("value 3");
			break;
		case 4: 
			System.out.println("value 4");
			break;
		default: 
			System.out.println("not valid");
		}
	}
	

}
